    private void jButtonGenerarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerarReporteActionPerformed
        // TODO add your handling code here:
        
        int cursoId = Integer.parseInt( this.jTextFieldIdCurso.getText());
        int SumaPagos=0;
        int ValorContrato=0;
        int deuda =0;
        int idEncargado = 0;
        int cantAlumnos =0;
        
        try {
            //Para establecer el modelo al JTable
            DefaultTableModel modelo = new DefaultTableModel();
            this.jTablePagos.setModel(modelo);
            //Para conectarnos a nuestra base de datos
            String url = "jdbc:oracle:thin:@ontourbd.cww6n5k3t8qv.us-east-2.rds.amazonaws.com:1521:orcl";
            // Establecemos los valores de cadena de conexi칩n, usuario y contrase침a
            cn = DriverManager.getConnection(url, "Portafolio02", "portafolio123");
            //Para ejecutar la consulta
            s = cn.createStatement();
            
            //obtener el id del encargado del curso
            rs = s.executeQuery("SELECT ENCARGADO_ID FROM ENCARGADO WHERE CURSO_ID_CURSO ="+cursoId);
            
            while(rs.next()){
            
            idEncargado= rs.getInt(1);
            }
            
            
            //obtener la informacion de los pagos
            //Ejecutamos la consulta y los datos lo almacenamos en un ResultSet
             rs = s.executeQuery("select PAGO_ID, FECHA_PAGO,MONTO_PAGO,DESCRIPCION from PAGO WHERE ENCARGADO_ENCARGADO_ID ="+idEncargado);
            //Obteniendo la informacion de las columnas que estan siendo consultadas
            ResultSetMetaData rsMd = rs.getMetaData();
            //La cantidad de columnas que tiene la consulta
            int cantidadColumnas = rsMd.getColumnCount();
            //Establecer como cabezeras el nombre de las colimnas
            for (int i = 1; i <= cantidadColumnas; i++) {
             modelo.addColumn(rsMd.getColumnLabel(i));
            }
            //Creando las filas para el JTable
            while (rs.next()) {
             Object[] fila = new Object[cantidadColumnas];
             for (int i = 0; i < cantidadColumnas; i++) {
               fila[i]=rs.getObject(i+1);
             }
             modelo.addRow(fila);
            }
            
            //obtener cantidad de alumnos
            rs = s.executeQuery("SELECT COUNT(ALUMNO_ID) FROM ALUMNO WHERE CURSO_ID_CURSO ="+cursoId);
            
            while(rs.next()){
            
            cantAlumnos= rs.getInt(1);
            }
            
            
            //obtener valor del contrato
            rs = s.executeQuery("SELECT TOTAL FROM CONTRATO WHERE ENCARGADO_ENCARGADO_ID ="+idEncargado);
            
            while(rs.next()){
            
            ValorContrato= rs.getInt(1);
            }
            this.jLabelValorTour.setText(" "+(ValorContrato*cantAlumnos));
            
            //obtener el total de pagos del curso
            rs = s.executeQuery("SELECT SUM(MONTO_PAGO) FROM PAGO WHERE ENCARGADO_ENCARGADO_ID ="+idEncargado);
            
            while(rs.next()){
            
            SumaPagos= rs.getInt(1);
            }
            this.jLabelSumaTotallPagos.setText(" "+SumaPagos);
            
            
            //obtener el total de la deuda
            rs = s.executeQuery("SELECT SUM(DEUDA) FROM ALUMNO WHERE CURSO_ID_CURSO ="+cursoId);
            
            while(rs.next()){
            
            deuda= rs.getInt(1);
            }
            this.jLabelDeuda.setText(" "+deuda);
            rs.close();
            cn.close();
            limpiarCampos();
       } catch (Exception ex) {
        ex.printStackTrace();
       }
        
    }//GEN-LAST:event_jButtonGenerarReporteActionPerformed

